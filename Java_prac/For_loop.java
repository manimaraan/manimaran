package Java_prac;

public class For_loop {
public static void main(String[] args) {
	for (int k = 1; k <=1; k++) {
		System.out.println(k);
		for (int i = 1; i <=2; i++) {
			System.out.print(i);
			
		}
		System.out.println();
		for (int l = 1; l <=3; l++) {
			System.out.print(l);
			
		}
		System.out.println();
		
	}
}
}
